function setLanguage(lang) {
  const dict = translations[lang] || translations.th;
  document.documentElement.lang = lang;
  localStorage.setItem('zuzie-lang', lang);

  document.querySelectorAll('[data-i18n]').forEach((node) => {
    const key = node.dataset.i18n;
    if (dict[key]) node.textContent = dict[key];
  });

  document.querySelectorAll('[data-i18n-placeholder]').forEach((node) => {
    const key = node.dataset.i18nPlaceholder;
    if (dict[key]) node.setAttribute('placeholder', dict[key]);
  });

  langButtons.forEach((button) => {
    const active = button.dataset.lang === lang;
    button.setAttribute('aria-pressed', active ? 'true' : 'false');
    button.classList.toggle('bg-reseda', active);
    button.classList.toggle('text-white', active);
  });
}

menuButton?.addEventListener('click', () => {
  const isOpen = mobileMenu.classList.toggle('hidden') === false;
  menuButton.setAttribute('aria-expanded', String(isOpen));
});

document.querySelectorAll('[data-mobile-menu] a').forEach((link) => {
  link.addEventListener('click', () => {
    mobileMenu.classList.add('hidden');
    menuButton?.setAttribute('aria-expanded', 'false');
  });
});

langButtons.forEach((button) => {
  button.addEventListener('click', () => setLanguage(button.dataset.lang));
});

document.querySelectorAll('[data-icon]').forEach((node) => {
  node.innerHTML = iconMap[node.dataset.icon] || '';
});

mockForms.forEach((form) => {
  form.addEventListener('submit', (event) => {
    event.preventDefault();
    const input = form.querySelector('input[type="email"]');
    if (input && !input.checkValidity()) {
      input.reportValidity();
      return;
    }
    form.reset();
    form.dataset.submitted = 'true';
  });
});

setLanguage(localStorage.getItem('zuzie-lang') || 'th');
