          <p class="text-xl font-medium text-ink mb-8 leading-relaxed" data-i18n="articleIntro">
            ความสัมพันธ์ควรเป็นพื้นที่ที่ทำให้เรารู้สึกปลอดภัย อบอุ่น และอุ่นใจ แต่ถ้าคุณมีความสัมพันธ์ที่ทำให้รู้สึกเหนื่อย บั่นทอน หรือหมดคุณค่า อาจเป็นสัญญาณว่าคุณกำลังอยู่ในความสัมพันธ์ที่เป็นพิษ (Toxic Relationship)
          </p>

          <h3 id="point-1" class="text-2xl font-bold mt-10 mb-4" data-i18n="point1Title">1. คุณรู้สึกเหนื่อยใจตลอดเวลา</h3>
          <p data-i18n="point1Desc">
            คุณมักจะรู้สึกหมดพลังใจหลังจากใช้เวลาอยู่กับเขาหรือเธอ เหมือนต้องระวังคำพูดและการกระทำของตัวเองตลอดเวลาเพื่อไม่ให้เกิดปัญหา
          </p>

          <h3 id="point-2" class="text-2xl font-bold mt-10 mb-4" data-i18n="point2Title">2. เขาควบคุมคุณ</h3>
          <p data-i18n="point2Desc">
            อีกฝ่ายพยายามเข้ามามีบทบาทและตัดสินใจแทนคุณในทุกๆ เรื่อง ตั้งแต่เรื่องเล็กน้อยไปจนถึงเรื่องใหญ่ในชีวิต โดยไม่เคารพพื้นที่ส่วนตัวของคุณ
          </p>

          <h3 id="point-3" class="text-2xl font-bold mt-10 mb-4" data-i18n="point3Title">3. คุณถูกตำหนิอยู่เสมอ</h3>
          <p data-i18n="point3Desc">
            ไม่ว่าคุณจะทำอะไรก็ดูเหมือนจะผิดไปหมด และเมื่อเกิดปัญหา เขาจะปัดความรับผิดชอบและทำให้คุณรู้สึกว่าเป็นต้นเหตุของเรื่องแย่ๆ ทั้งหมด
          </p>

          <h3 id="point-4" class="text-2xl font-bold mt-10 mb-4" data-i18n="point4Title">4. เขาไม่เคยขอโทษ</h3>
          <p data-i18n="point4Desc">
            แม้ว่าเขาจะเป็นฝ่ายผิดอย่างชัดเจน แต่คุณแทบจะไม่ได้ยินคำว่า "ขอโทษ" ออกจากปากเขาเลย หรือถ้ามีก็เป็นเพียงการขอโทษแบบขอไปที
          </p>

          <h3 id="point-5" class="text-2xl font-bold mt-10 mb-4" data-i18n="point5Title">5. คุณรู้สึกไม่เป็นตัวเอง</h3>
          <p data-i18n="point5Desc">
            คุณต้องเปลี่ยนตัวเอง เปลี่ยนความชอบ หรือเลิกทำสิ่งที่รักเพียงเพื่อให้เขาพอใจ จนสุดท้ายคุณลืมไปแล้วว่าตัวตนที่แท้จริงของคุณเป็นอย่างไร
          </p>

          <h3 id="point-6" class="text-2xl font-bold mt-10 mb-4" data-i18n="point6Title">6. เขาเล่นตุกติกทางอารมณ์</h3>
          <p data-i18n="point6Desc">
            มีการใช้เทคนิคปั่นหัว (Gaslighting) เพื่อทำให้คุณสับสนในความทรงจำและความคิดของตัวเอง ทำให้คุณไม่มั่นใจในตัวเองอีกต่อไป
          </p>

          <h3 id="point-7" class="text-2xl font-bold mt-10 mb-4" data-i18n="point7Title">7. เขาแยกคุณจากคนรอบตัว</h3>
          <p data-i18n="point7Desc">
            เขาพยายามกีดกันไม่ให้คุณไปเจอเพื่อนหรือครอบครัว ทำให้คุณต้องพึ่งพาและมีแค่เขาคนเดียวในชีวิต
          </p>
          
          <h3 id="point-8" class="text-2xl font-bold mt-10 mb-4" data-i18n="point8Title">8. คุณให้อภัยซ้ำแล้วซ้ำเล่า</h3>
          <p data-i18n="point8Desc">
            แม้เขาจะทำผิดซ้ำๆ และสัญญาว่าจะเปลี่ยน แต่ทุกอย่างก็กลับมาสู่อีหรอบเดิม และคุณก็ยังคงให้อภัยเขาเรื่อยมา
          </p>

          <h3 id="point-9" class="text-2xl font-bold mt-10 mb-4" data-i18n="point9Title">9. คุณรู้สึกโดดเดี่ยว</h3>
          <p data-i18n="point9Desc">
            แม้จะมีเขาอยู่ข้างๆ แต่คุณกลับรู้สึกเหงาและโดดเดี่ยว ไม่สามารถพึ่งพาหรือพูดคุยเรื่องความรู้สึกลึกๆ กับเขาได้
          </p>

          <h3 id="point-10" class="text-2xl font-bold mt-10 mb-4" data-i18n="point10Title">10. คุณไม่ได้มองเห็นอนาคตร่วมกัน</h3>
          <p data-i18n="point10Desc">
            เมื่อมองไปข้างหน้า คุณไม่เห็นภาพชีวิตที่มีความสุขร่วมกับเขา มีแต่ความกังวลและหวาดกลัวว่าพรุ่งนี้จะต้องเจอกับอะไร
          </p>

